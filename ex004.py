dg = input('Digite algo: ')
print('O tipo primitivo e: {}.'.format(type(dg)))
print('E numerico? ', dg.isnumeric())
print('E alfanumerico? {}'.format(dg.isalnum()))
print('Esta em maiusculo? {}'.format(dg.isupper()))
print('Esta em Minusculo? {}'.format(dg.islower()))
print('Esta capitalizada? {}'.format(dg.istitle()))

